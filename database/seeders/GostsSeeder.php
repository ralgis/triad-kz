    public function run(): void
    {
        // Pass 1: create / update every record without self-FKs.
        foreach ($this->records() as $row) {
            Gost::updateOrCreate(['slug' => $row['slug']], $row['data']);
        }

        // Pass 2: resolve self-FKs now that all IDs exist.
        $bySlug = Gost::query()->whereIn('slug', array_column($this->records(), 'slug'))
            ->get()
            ->keyBy('slug');

        foreach ($this->records() as $row) {
            $current = $bySlug[$row['slug']] ?? null;
            if ($current === null) {
                continue;
            }

            $updates = [];
            if (! empty($row['superseded_by'])) {
                $updates['superseded_by_id'] = $bySlug[$row['superseded_by']]?->id;
            }
            if (! empty($row['relates_to'])) {
                $updates['relates_to_gost_id'] = $bySlug[$row['relates_to']]?->id;
            }

            if ($updates !== []) {
                $current->update($updates);
            }
        }
    }

